public class J010OperadoresIncremento{
    public static void main(String[] args) {
        int edad = 46;
        System.out.println("Mi edad es de "+edad+" años");
        edad++;
        System.out.println("Mi edad es de "+edad+" años");
        edad--;
        System.out.println("Mi edad es de "+edad+" años");
    }
}
