public class J011OperadoresAbreviados{
    public static void main(String[] args) {
        int edad = 46;
        System.out.println("Mi edad es de "+edad+" años");
        edad += 5;
        System.out.println("Mi edad es de "+edad+" años");
        edad -= 5;
        System.out.println("Mi edad es de "+edad+" años");
        edad *= 5;
        System.out.println("Mi edad es de "+edad+" años");
        edad /= 5;
        System.out.println("Mi edad es de "+edad+" años");
    }
}
